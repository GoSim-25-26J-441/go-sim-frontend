export const getRegionDisplayName = (
  regionCode: string,
  provider: "aws" | "azure" | "gcp"
): string => {
  if (!regionCode || regionCode.trim() === "") return "No Region";

  if (provider === "azure") {
    return formatAzureRegion(regionCode);
  } else if (provider === "aws") {
    return formatAWSRegion(regionCode);
  }
  return regionCode
    .split(/(?=[A-Z])|(?<=[a-z])(?=[0-9])/)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
};
